    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow= tblProfile.getSelectedRow();

        if(selectedRow >= 0){
            int confirmed = JOptionPane.showConfirmDialog(null, "Are you Sure you want to delete the record?","Confirm Delete",JOptionPane.YES_NO_OPTION);
            if(confirmed == JOptionPane.YES_OPTION){
                Person pObj  = (Person)tblProfile.getValueAt(selectedRow, 0);
                personRecord.deleteProfile(pObj);
                JOptionPane.showMessageDialog(null,"Profile has been removed!");
                populateTable();
            }
        }else{
            JOptionPane.showMessageDialog(null,"Please select any row!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

